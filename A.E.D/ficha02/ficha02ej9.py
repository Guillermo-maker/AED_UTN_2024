
ancho = float(input("Ingrese el ancho del rectángulo en metros: "))
alto = float(input("Ingrese el alto del rectángulo en metros: "))

perimetro = 2 * (ancho + alto)
superficie = ancho * alto

print("El perímetro del rectángulo es:", perimetro, "metros")
print("La superficie del rectángulo es:", superficie, "metros cuadrados")

