def collatz(n):
    long_orbita = 1
    suma_valores = n
    lista = [n]
    while n != 1:
        long_orbita += 1
        if n % 2 != 0:
            n = 3 * n + 1
        else:
            n = n // 2
        lista.append(n)
        suma_valores += n

    promedio = suma_valores / long_orbita

    print("Orbita de n:", lista)
    print("Longitud de la órbita:(valores calculados incluyendo al 13 y al 1) ", long_orbita)
    print("Promedio de todos los valores de la órbita:", promedio)
    print("Mayor de los números en esa órbita: ", max(lista))


def main():
    n = int(input("Ingrese un numero: "))
    collatz(n)


if __name__ == "__main__":
    main()

